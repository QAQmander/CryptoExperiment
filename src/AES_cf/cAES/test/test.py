#!/usr/bin/env python3
# -*- coding: utf-8 -*-

import cAES as aes

key = [
    0xde, 0xad, 0xbe, 0xef, 
    0xde, 0xad, 0xbe, 0xef, 
    0xde, 0xad, 0xbe, 0xef, 
    0xde, 0xad, 0xbe, 0xef
]
plain = [
    0x01, 0x23, 0x45, 0x67, 
    0x89, 0xab, 0xcd, 0xef, 
    0xfe, 0xdc, 0xba, 0x98, 
    0x76, 0x54, 0x32, 0x10
]

aes.tell_me_the_devil_secret(*key)

new_cipher = aes.encrypt(*plain)

for i in new_cipher:
    print(hex(i)[2:], end=' ')
print()

# cipher = [0x3b, 0x68, 0x81, 0x4e, 0xb7, 0x1a, 0x58, 0x4c, 0x7c, 0x2a, 0x43, 0x65, 0xa9, 0xb4, 0x94, 0x95]
new_plain = aes.decrypt(*new_cipher)

for i in new_plain:
    print(hex(i)[2:], end=' ')
print()
